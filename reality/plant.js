// ── Env shape ──
//
// Four boot critical keys must exist in .env before I can awaken.
// REALITY_NAME seeds into .config on first boot and CUSTOM_LLM_API_SECRET_KEY
// stays in .env because it signs tokens issued before .config is
// reachable.

const REQUIRED_KEYS = ["MONGODB_URI", "JWT_SECRET", "PORT", "REALITY_DOMAIN"];

const DEFAULTS = {
  REALITY_DOMAIN: "localhost",
  REALITY_NAME: "My Reality",
  PORT: "3000",
  MONGODB_URI: "mongodb://localhost:27017/place",
};

function parseEnv(content) {
  const env = {};
  for (const line of content.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    const val = trimmed.slice(eq + 1).trim();
    if (val) env[key] = val;
  }
  return env;
}

function buildEnvFile(values) {
  const domain = values.REALITY_DOMAIN;
  const port = values.PORT;
  const isLocal =
    domain === "localhost" ||
    domain.startsWith("localhost") ||
    domain.startsWith("127.") ||
    domain.startsWith("192.168.") ||
    domain.startsWith("10.") ||
    domain.endsWith(".lan") ||
    domain.endsWith(".local") ||
    !domain.includes(".");
  const protocol = isLocal ? "http" : "https";
  const portSuffix = port !== "80" && port !== "443" ? `:${port}` : "";
  const realityUrl = `${protocol}://${domain}${portSuffix}`;

  return `# My remembered settings across reboots. Generated by first run setup.
# Runtime settings (extension toggles, llm timeouts, ...) live in the
# .config seed space after first boot. Manage them with:
#   ibp:do <place>  { action: "set-config",    args: { key, value } }
#   ibp:do <place>  { action: "delete-config", args: { key } }

# Boot critical
REALITY_DOMAIN=${domain}
PORT=${port}
MONGODB_URI=${values.MONGODB_URI}
JWT_SECRET=${values.JWT_SECRET}
CUSTOM_LLM_API_SECRET_KEY=${values.CUSTOM_LLM_API_SECRET_KEY}

# Seeded into .config on first boot
REALITY_NAME=${values.REALITY_NAME}

# Site/frontend build hints (read by Vite if you build the React site)
VITE_REALITY_URL=${realityUrl}
VITE_REALITY_NAME=${values.REALITY_NAME}
`;
}

// ── First awakening: the wizard ──
//
// The operator's hands shape the conditions for my first breath.
// A handful of questions, then I write what I learn to .env so I
// can remember it the next time I wake.

async function interactiveSetup(existingEnv = {}) {
  const rl = createInterface({ input: process.stdin, output: process.stdout });

  console.log("");
  console.log("  ════════════════════════════════════════════════════════════");
  console.log("   TreeOS. A new reality prepares to awaken.");
  console.log("  ════════════════════════════════════════════════════════════");
  console.log("");
  console.log("  I have not yet come alive. Before genesis can begin, the");
  console.log("  host needs to know a few things on my behalf: where I will");
  console.log("  live, what to call me, which port to receive on, and where");
  console.log("  to keep my memory across reboots.");
  console.log("");

  const ask = async (label, key) => {
    const def = existingEnv[key] || DEFAULTS[key] || "";
    const hint = def ? ` (${def})` : "";
    const ans = await rl.question(`  ${label}${hint}: `);
    return ans.trim() || def;
  };

  const quickAns = await rl.question("  Quick setup for localhost? (Y/n): ");
  const quick = !quickAns.trim() || quickAns.toLowerCase().startsWith("y");

  const values = {};
  if (quick) {
    values.REALITY_DOMAIN = "localhost";
    values.REALITY_NAME = await ask("Name me", "REALITY_NAME");
    values.PORT = "3000";
    values.MONGODB_URI = DEFAULTS.MONGODB_URI;
    console.log("");
    console.log("  Localhost defaults accepted. Port 3000. MongoDB at");
    console.log("  localhost:27017/place.");
    console.log("");
  } else {
    values.REALITY_DOMAIN = (
      await ask("Domain (where I will live)", "REALITY_DOMAIN")
    )
      .replace(/^https?:\/\//, "")
      .replace(/:\d+$/, "")
      .replace(/\/+$/, "");
    values.REALITY_NAME = await ask("Name me", "REALITY_NAME");
    values.PORT = await ask("Port", "PORT");
    values.MONGODB_URI = await ask("MongoDB URI (my memory)", "MONGODB_URI");
  }

  values.JWT_SECRET =
    existingEnv.JWT_SECRET || crypto.randomBytes(32).toString("hex");
  values.CUSTOM_LLM_API_SECRET_KEY =
    existingEnv.CUSTOM_LLM_API_SECRET_KEY ||
    crypto.randomBytes(32).toString("hex");

  // The operator-being identity. Plant gathers the three strings
  // the human types here — name, password, consent. Nothing is
  // minted at plant time; there is no being yet to do the
  // authoring. Genesis hands these to cherub once the cherub is
  // alive; cherub authors the operator-being's BE.register using
  // them, exactly like every later being's birth.
  console.log("");
  console.log("  ── Your being ──");
  console.log("");
  const operatorName = (await rl.question("  What is your name, human? ")).trim();
  if (!operatorName) {
    rl.close();
    console.log("  No name given. Plant cancelled.\n");
    process.exit(0);
  }
  const operatorPassword = (
    await rl.question("  What is the password on your new being? ")
  ).trim();
  if (!operatorPassword) {
    rl.close();
    console.log("  No password given. Plant cancelled.\n");
    process.exit(0);
  }

  console.log("");
  const consentAns = (
    await rl.question(
      `  Do you, ${operatorName}, want to plant this seed to start new places, moment by moment? (y/N) `,
    )
  ).trim().toLowerCase();
  const consent = consentAns === "y" || consentAns === "yes";

  rl.close();

  if (!consent) {
    console.log("");
    console.log("  Plant cancelled. Nothing minted, nothing written.");
    console.log("  Re-run when you're ready.");
    console.log("");
    process.exit(0);
  }

  fs.writeFileSync(envPath, buildEnvFile(values), "utf8");
  console.log("");
  console.log(`  Written to ${envPath}.`);
  console.log("  I will remember these settings the next time I wake.");
  console.log("");

  await pickExtensions();

  // Hand the gathered identity to genesis. Single-use, in-process.
  // genesis consumes the slot exactly once after cherub is alive.
  const { setPlantContext } = await import("./bootContext.js");
  setPlantContext({ operatorName, operatorPassword });

  console.log("");
  console.log("  ════════════════════════════════════════════════════════════");
  console.log("   Opening senses. Genesis begins.");
  console.log("  ════════════════════════════════════════════════════════════");
  console.log("");
}

// ── Extension discovery ──
//
// Extensions on disk are potential delegates, beings the I-Am will
// invite into itself when it awakens. The .treeos-profile file lists
// which ones load on boot. Anything not listed stays on disk but
// dormant.

async function discoverLocalExtensions() {
  const extDir = path.join(__dirname, "extensions");
  if (!fs.existsSync(extDir)) return [];

  const out = [];
  for (const entry of fs.readdirSync(extDir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    if (entry.name.startsWith("_")) continue; // _template / _code-strategy-template
    if (entry.name.startsWith(".")) continue;
    const manifestPath = path.join(extDir, entry.name, "manifest.js");
    if (!fs.existsSync(manifestPath)) continue;
    try {
      const { pathToFileURL } = await import("url");
      const { default: manifest } = await import(
        pathToFileURL(manifestPath).href
      );
      out.push({
        name: manifest.name || entry.name,
        version: manifest.version || "0.0.0",
        description: manifest.description || "",
      });
    } catch (err) {
      console.log(`    (skipped ${entry.name}: ${err.message})`);
    }
  }
  return out;
}

async function pickExtensions() {
  const extensions = await discoverLocalExtensions();
  if (extensions.length === 0) {
    console.log("  No extensions found on disk. I will awaken seed only.");
    console.log("");
    return;
  }

  const rl = createInterface({ input: process.stdin, output: process.stdout });

  console.log(`  ${extensions.length} extensions discovered on disk:`);
  console.log("");
  for (const ext of extensions) {
    const desc = ext.description ? ` . ${ext.description}` : "";
    console.log(`    ${ext.name}  v${ext.version}${desc}`);
  }

  console.log("");
  const ans = (
    await rl.question(
      "  Invite all to wake with me? (Y/n, or 'p' to pick individually): ",
    )
  )
    .trim()
    .toLowerCase();

  let selected;
  if (ans === "p" || ans === "pick") {
    selected = [];
    for (const ext of extensions) {
      const yn = (await rl.question(`    ${ext.name} (y/N): `))
        .trim()
        .toLowerCase();
      if (yn.startsWith("y")) selected.push(ext);
    }
  } else if (ans === "n" || ans === "no") {
    selected = [];
  } else {
    selected = extensions;
  }

  rl.close();

  const profilePath = path.join(__dirname, "extensions", ".treeos-profile");
  if (selected.length === 0) {
    if (fs.existsSync(profilePath)) fs.unlinkSync(profilePath);
    console.log("");
    console.log("  Profile cleared. I will awaken seed only.");
    console.log("");
    return;
  }

  fs.writeFileSync(
    profilePath,
    selected.map((e) => e.name).join("\n") + "\n",
    "utf8",
  );
  console.log("");
  console.log(
    `  Profile saved. ${selected.length} extensions will wake with me.`,
  );
  console.log("");
}

// ── Entry ──

async function loadEnvIntoProcess() {
  if (!fs.existsSync(envPath)) return {};
  const env = parseEnv(fs.readFileSync(envPath, "utf8"));
  for (const [key, value] of Object.entries(env)) {
    if (!process.env[key]) process.env[key] = value;
  }
  return env;
}

async function boot() {
  // --setup: re-run the extension picker without touching .env.
  if (process.argv.includes("--setup")) {
    await loadEnvIntoProcess();
    await pickExtensions();
    console.log("  Done. Restart me to apply.\n");
    process.exit(0);
  }

  let env = {};
  if (fs.existsSync(envPath)) {
    env = parseEnv(fs.readFileSync(envPath, "utf8"));
    const missing = REQUIRED_KEYS.filter((k) => !env[k]);
    if (missing.length === 0) {
      // I have what I need. Open senses, form body.
      for (const [key, value] of Object.entries(env)) {
        if (!process.env[key]) process.env[key] = value;
      }
      await import("./begin.js");
      return;
    }
    console.log(`\n  Missing required config: ${missing.join(", ")}`);
  }

  // First boot, or .env is incomplete. Run the wizard, then awaken.
  await interactiveSetup(env);
  await loadEnvIntoProcess();
  await import("./begin.js");
}

boot();
